def dispatcher(operator, x, y):
    if operator == 'add':
        return x + y
    elif operator == 'sub':
        return x - y
    elif operator == 'mul':
        return x * y
    elif operator == 'div':
        return x / y
    else:
        return None


print(dispatcher('mul', 3, 5))
print(dispatcher('abc', 3, 5))
